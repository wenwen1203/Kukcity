package kukcity.horizon;



public class PubInfo {
	public static  String id,pubId, pubName, pubAdress, pubRating,pubReview;
	
	public void setId(String id){
		this.id=id;
	}
	
	public String getId(){
		
		return id;
	}
	
	
	public void setPubId(String pubId){
		this.pubId=pubId;
	}
	
	public String getPubId(){
		return pubId;
		
	}
	
	public void setPubName(String pubName){
		this.pubName=pubName;
			
	}
	
	public String getpubName(){
		return pubName;
	}
	
	public void setpubAdress(String pubAdress){
		this.pubAdress=pubAdress;
	}
	
	public String getPubAdress(){
		return pubAdress;
	}
	
	public void setPubRating(String pubRating){
		this.pubRating=pubRating;
	}
	
	public String getPubRating(){
		return pubRating;
		
	}
	
	public void setPubReview(String pubReview){
		this.pubReview=pubReview;
	}
	
	public String getPubReview(){
		return pubReview;
		
	}
	
}	





